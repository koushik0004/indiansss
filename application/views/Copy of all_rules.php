                	      <p dir="ltr">The title page must include: (a) Title of the paper, (b) Name(s) of the author(s), (c) Affiliation(s) and Designations of the author(s), (d) e-mail Address(es), Telephone (s) and Fax number (s) of the author (s), and (d) Photograph (s) of the author (s).</p>
